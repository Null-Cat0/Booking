package es.unex.pi.model;

public class Message {
	long id;
	long ids;
	long idr;
	String texto;
	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	public long getIds() {
		return ids;
	}
	public void setIds(long ids) {
		this.ids = ids;
	}
	public long getIdr() {
		return idr;
	}
	public void setIdr(long idr) {
		this.idr = idr;
	}
	public String getTexto() {
		return texto;
	}
	public void setTexto(String text) {
		this.texto = text;
	}

}
